/**
  * op_add - Finds sum
  * @a: First int
  * @b: Second int
  * Return: a + b
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - Finds difference
  * @a: First int
  * @b: Second int
  * Return: a - b
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - Finds multiplication
  * @a: First int
  * @b: Second int
  * Return: a * b
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - Finds division
  * @a: First int
  * @b: Second int
  * Return: a / b
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - Finds modulus
  * @a: First int
  * @b: Second int
  * Return: a % b
  */
int op_mod(int a, int b)
{
	return (a % b);
}



